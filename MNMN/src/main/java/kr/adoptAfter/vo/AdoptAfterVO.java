package kr.adoptAfter.vo;

import java.sql.Date;

public class AdoptAfterVO {
	
	private int after_num;
	private int after_pet_num;
	private String after_title;
	private String after_content;
	private Date after_date;
	private int after_member_num;
	private String after_photo;
	
	public int getAfter_num() {
		return after_num;
	}
	public void setAfter_num(int after_num) {
		this.after_num = after_num;
	}
	public int getAfter_pet_num() {
		return after_pet_num;
	}
	public void setAfter_pet_num(int after_pet_num) {
		this.after_pet_num = after_pet_num;
	}
	public String getAfter_title() {
		return after_title;
	}
	public void setAfter_title(String after_title) {
		this.after_title = after_title;
	}
	public String getAfter_content() {
		return after_content;
	}
	public void setAfter_content(String after_content) {
		this.after_content = after_content;
	}
	public Date getAfter_date() {
		return after_date;
	}
	public void setAfter_date(Date after_date) {
		this.after_date = after_date;
	}
	public int getAfter_member_num() {
		return after_member_num;
	}
	public void setAfter_member_num(int after_member_num) {
		this.after_member_num = after_member_num;
	}
	public String getAfter_photo() {
		return after_photo;
	}
	public void setAfter_photo(String after_photo) {
		this.after_photo = after_photo;
	}
	
	
}
